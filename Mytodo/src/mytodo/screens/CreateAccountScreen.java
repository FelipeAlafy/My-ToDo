    private void btnCreateAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateAccountActionPerformed
        // TODO add your handling code here:
        boolean created = false;
        createAccount ca = new createAccount();
        if(!this.FieldName.getText().isEmpty()){
             created = ca.createAccount(this.FieldName.getText(), PasswordConverterToString.passwordToString(this.FieldConfirmPass.getPassword())); 
        }
        
        if(!created){
            JOptionPane.showMessageDialog(null, "Erro ao criar conta.");
        } else {
            this.dispose();
            ls.setVisible(true);
        }
    }//GEN-LAST:event_btnCreateAccountActionPerformed

